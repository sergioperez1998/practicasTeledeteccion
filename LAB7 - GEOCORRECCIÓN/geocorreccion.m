function img2 = geocorrecion(img1, banda)
    img1 = img1(:, :, banda);
%     xGCP = [529193.6; 469963.9; 259395.6; 698143.5; 574790; 262511.9; 734115.1; 500164.6; 460018.4; ...
%         516893.4; 464295.1; 613738.4; 562139.7];
%     yGCP = [4682303.6; 4369167.5; 4287656.5; 4161873.6; 4062774.1; 3989189; 4053916.5; 4093626.6; 4282216; ...
%         4494109.8; 4755870.6; 5119642.9; 5191048.3];
%     fGCP = [1777; 1707; 1532; 1431; 1271; 881; 817; 588; 602; 704; 776; 1524; 1550;];
%     cGCP = [3014; 3298; 3324; 3442; 3485; 3532; 3445; 3356; 3189; 3013; 2813; 2840; 2557;];
    xGCP = [564182.660; 619428.166; -8699.905; -1211.293; -47835.184; -31344.329; 201608.988; ...
        289282.102; 576581.823; 702631.129; 777819.735; 971756.716; 1018974.579];
    yGCP = [5189100.568; 5105847.492; 4772141.250; 4459524.870; 4276722.581; 4112783.164; ...
        4048541.418; 4001266.879; 4065272.753; 4169356.976; 4296246.791; 4383322.393; 4703410.408];
    cGCP = [1259; 1294; 511; 441; 352; 327; 560; 645; 1015; 1166; 1266; 1435; 1512];
    fGCP = [50; 136; 333; 608; 772; 912; 1011; 1067; 1053; 984; 880; 836; 558];
    
    o = ones(length(xGCP), 1);
    a = regress(fGCP, [o, xGCP, yGCP]);
    b = regress(cGCP, [o, xGCP, yGCP]);
    
    [F, C] = size(img1);
    img2 = uint8(zeros(F+500, C+500));
    x = -750000;
    y = 5400000;
    xTemp = x;
    for f = 1:F+500
        for c = 1:C+500
            fn = a(1) + a(2) * x + a(3) * y;
            cn = b(1) + b(2) * x + b(3) * y;
            x = x + 1000;
            if fn >= 1 && fn <= F && cn >= 1 && cn <= C
                img2(f, c) = img1(round(fn), round(cn));
            end
        end
        x = xTemp;
        y = y - 1000;
    end
    imshow(img2);
end